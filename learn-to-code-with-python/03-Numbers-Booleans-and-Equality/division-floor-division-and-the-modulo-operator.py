# Normal division - returns float.
print(15 / 3)
print(14 / 3)

# Floor division - rounds down and returns 
# integer datatype.
print(14 // 3)

# Modulo - returns the remainder of a division.
print(14 % 3)
print(15 % 3)
print(16 % 3)
# If left number is less than right number, 
# python will return left number.
print(3 % 5)